package de.keybird.beagle.rest;

import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Paths;
import java.util.Collections;
import java.util.List;
import java.util.stream.Collectors;

import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.RestController;

@RestController
@RequestMapping("/beagles")
public class BeagleRestController {

    @RequestMapping(method= RequestMethod.GET)
    public List<String> getBeagles() {
        try {
            return Files.list(Paths.get("/img/beagles"))
                    .filter(p -> !Files.isDirectory(p)).map(p -> p.getFileName().toString())
                    .collect(Collectors.toList());
        } catch (IOException e) {
            return Collections.emptyList();
        }
    }
}
