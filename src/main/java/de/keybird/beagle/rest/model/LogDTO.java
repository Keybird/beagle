package de.keybird.beagle.rest.model;

import java.util.Date;

import de.keybird.beagle.jobs.persistence.LogEntity;
import de.keybird.beagle.jobs.xxxx.LogLevel;

public class LogDTO {
    private Long id;

    private Date date = new Date();

    private String message;

    private LogLevel level;

    public LogDTO() {

    }

    public LogDTO(LogEntity logEntity) {
        setId(logEntity.getId());
        setDate(logEntity.getDate());
        setLevel(logEntity.getLevel());
        setMessage(logEntity.getMessage());
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public LogLevel getLevel() {
        return level;
    }

    public void setLevel(LogLevel level) {
        this.level = level;
    }
}
